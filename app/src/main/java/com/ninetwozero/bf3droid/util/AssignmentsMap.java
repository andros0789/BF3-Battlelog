    public static int assignmentDrawable(String key) {
        return ASSIGNMENT_RESOURCES.containsKey(key) ? ASSIGNMENT_RESOURCES.get(key).get(0) : R.drawable.assignment_locked;
    }

    public static List<Integer> rewardResources(String key) {
        List<Integer> assignmentResources = ASSIGNMENT_RESOURCES.get(key);
        if (assignmentResources.isEmpty()) {
            return new ArrayList<Integer>();
        } else {
            return assignmentResources.subList(1, assignmentResources.size());
        }
    }
